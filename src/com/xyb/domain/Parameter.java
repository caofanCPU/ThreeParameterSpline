package com.xyb.domain;

/**
 * 三次参数样条曲线参数列表
 * A，B，C，D，E，F，G，H
 * @author CY_XYZ
 *
 */
public class Parameter {
	private int ID;
	private double xA;
	private double xB;
	private double xC;
	private double xD;
	private double yE;
	private double yF;
	private double yG;
	private double yH;
	public double getxA() {
		return xA;
	}
	public void setxA(double xA) {
		this.xA = xA;
	}
	public double getxB() {
		return xB;
	}
	public void setxB(double xB) {
		this.xB = xB;
	}
	public double getxC() {
		return xC;
	}
	public void setxC(double xC) {
		this.xC = xC;
	}
	public double getxD() {
		return xD;
	}
	public void setxD(double xD) {
		this.xD = xD;
	}
	public double getyE() {
		return yE;
	}
	public void setyE(double yE) {
		this.yE = yE;
	}
	public double getyF() {
		return yF;
	}
	public void setyF(double yF) {
		this.yF = yF;
	}
	public double getyG() {
		return yG;
	}
	public void setyG(double yG) {
		this.yG = yG;
	}
	public double getyH() {
		return yH;
	}
	public void setyH(double yH) {
		this.yH = yH;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	
}
